void gotoXY(int x, int y)
{
COORD coord;
coord.X= x;
coord.Y= y;
SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), coord);
}

void movement()
{
    int x=1;
    int y=1;
    int xBefore= 1;
    int yBefore= 1;

    while(true)
    {
        gotoXY(xBefore, yBefore);
        cout<<" ";

        gotoXY(x,y);
        cout<<"X";

        inputKey =_getch();
        xBefore= x;
        yBefore= y;

        if(inputKey == 'w')
            y -= 1;
        else if(inputKey == 's')
            y += 1;
        else if(inputKey == 'd')
            x += 1;
        else if(inputKey == 'a')
            x -=1;

        if (mapArrayOne[y][x] ==1)
        {
            x=xBefore;
            y=yBefore;

        }
    }
}
